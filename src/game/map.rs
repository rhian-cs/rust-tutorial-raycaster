pub const MAP: [u16; 8] = [
    0b1111111111111111,
    0b1000000000000001,
    0b1000011111100001,
    0b1000010000000001,
    0b1000010000100001,
    0b1000010000000001,
    0b1000000000000001,
    0b1111111111111111,
];

pub fn point_in_wall(x: f32, y: f32) -> bool {
    match MAP.get(y as usize) {
        Some(line) => (line & (0b1 << x as usize)) != 0,
        None => true, // Out of bounds address are always considered a wall
    }
}
